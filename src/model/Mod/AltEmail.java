package model.Mod;

public class AltEmail {
    private String email;
    private int idH;

    public AltEmail(String email, int idH) {
        this.email = email;
        this.idH = idH;
    }
    
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public int getIdH() {
        return idH;
    }
    public void setIdH(int idH) {
        this.idH = idH;
    }


}
