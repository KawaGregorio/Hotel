package model.Mod;

public class AltCPF {
    private int cpf;
    private int idH;

    public AltCPF(int cpf, int idH) {
        this.cpf = cpf;
        this.idH = idH;
    }
    
    public int getCpf() {
        return cpf;
    }
    public void setCpf(int cpf) {
        this.cpf = cpf;
    }
    public int getIdH() {
        return idH;
    }
    public void setIdH(int idH) {
        this.idH = idH;
    }

    
}
